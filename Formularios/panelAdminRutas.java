    public void limpiarTabla(){
        rutas.removeAll(rutas);
        for (int i = 0; i < tblRuta.getRowCount(); i++) {
            tabla.removeRow(i);
            i-=1;
        }
    }
    
    public void setearHora(int horas, int minutos, int segundos){
        calendar = Calendar.getInstance();
        calendar.set(Calendar.HOUR_OF_DAY, horas);
        calendar.set(Calendar.MINUTE, minutos);
        calendar.set(Calendar.SECOND, segundos);
        hora = calendar.getTime();
        sm = new SpinnerDateModel(hora, null, null, Calendar.HOUR_OF_DAY);
        spinnerTiempo.setModel(sm);
        de = new JSpinner.DateEditor(spinnerTiempo, "HH:mm:ss");
        spinnerTiempo.setEditor(de);
    }
    
    public String obtenerTiempo(String horaS) {
        String[] horaStr = horaS.split(" ");  // separo el string ingresado
        String horaAproximada = horaStr[3];
        return horaAproximada;      // devuelve el número de espacios de la cadena introducida
    }

