    public void llenarTabla(){
        cnx.conectar();
        limpiarTabla();
        rutas.addAll(cnx.consultarRutas());
        for (int i = 0; i < rutas.size(); i++) {
            ruta[0] = "" + rutas.get(i).getIdRutas();
            ruta[1] = rutas.get(i).getOrigen();
            ruta[2] = rutas.get(i).getDestino();
            ruta[3] = "" + rutas.get(i).getTiempoAproximado();
            ruta[4] = rutas.get(i).getCosto();
            ruta[5] = "" + rutas.get(i).getFechaViaje();
            ruta[6] = "" + rutas.get(i).getHoraViaje();
            ruta[7] = "" + rutas.get(i).getIdBus();
            tabla.addRow(ruta);
        }
    }
