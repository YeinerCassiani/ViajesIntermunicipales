    private void txtBusquedaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBusquedaKeyTyped
        txtBusqueda.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent ke) {
                trs.setRowFilter(RowFilter.regexFilter(txtBusqueda.getText()));
            }
        });
        trs = new TableRowSorter(tabla);
        tblRuta.setRowSorter(trs);
    }//GEN-LAST:event_txtBusquedaKeyTyped

    private void tblRutaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRutaMouseClicked
        indiceTabla = Integer.parseInt(tabla.getValueAt(tblRuta.getSelectedRow(), 0).toString()) - 1; 
        for (int i = 0; i < rutas.size(); i++) {
            if (indiceTabla + 1 == rutas.get(i).getIdRutas()) {
                indiceArray = i;
            }
        }
        txtSalida.setText(rutas.get(indiceArray).getOrigen());
        txtDestino.setText(rutas.get(indiceArray).getDestino());
        setearHora(rutas.get(indiceArray).getTiempoAproximado().getHours(),rutas.get(indiceArray).getTiempoAproximado().getMinutes(),rutas.get(indiceArray).getTiempoAproximado().getSeconds());
        txtCosto.setText(rutas.get(indiceArray).getCosto());
        txtFechaViaje.setDate(rutas.get(indiceArray).getFechaViaje());
        txtHoraViaje.setText("" + rutas.get(indiceArray).getHoraViaje());
        txtIDBus.setText("" + rutas.get(indiceArray).getIdBus());
    }//GEN-LAST:event_tblRutaMouseClicked

