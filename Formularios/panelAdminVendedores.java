    public void llenarTabla(){
        cnx.conectar();
        limpiarTabla();
        usuario.addAll(cnx.consultarUsuario("Vendedor"));
        for (int i = 0; i < usuario.size(); i++) {
            cliente[0] = "" + usuario.get(i).getId_usuario();
            cliente[1] = usuario.get(i).getDocumento();
            cliente[2] = usuario.get(i).getNombre();
            cliente[3] = usuario.get(i).getApellido();
            cliente[4] = usuario.get(i).getUsuario();
            cliente[5] = usuario.get(i).getContrasena();
            cliente[6] = usuario.get(i).getTelefono();
            cliente[7] = usuario.get(i).getEmail();
            tabla.addRow(cliente);
        }
    }
    
    public void limpiarTabla(){
        usuario.removeAll(usuario);
        for (int i = 0; i < tblVendedor.getRowCount(); i++) {
            tabla.removeRow(i);
            i-=1;
        }
    }
    
