    private void txtBusquedaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBusquedaKeyTyped
        txtBusqueda.addKeyListener(new KeyAdapter() {
            @Override
            public void keyReleased(KeyEvent ke) {
                trs.setRowFilter(RowFilter.regexFilter(txtBusqueda.getText()));
            }
        });
        
        trs = new TableRowSorter(tabla);
        tblVendedor.setRowSorter(trs);
    }//GEN-LAST:event_txtBusquedaKeyTyped

    private void btnCrearMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCrearMouseClicked
        cnx.conectar();
        llenarTabla();
        if (cnx.verificarUsuario(txtDocumento.getText())) {
            JOptionPane.showMessageDialog(this, "El usuario ya se encuentra registrado.", "Error", 0);
        }else{
            boolean correcto = cnx.ingresarUSuario(txtDocumento.getText(), txtNombre.getText() + " " + txtApellido.getText(), txtContrasena.getText(), txtTelefono.getText(), txtCorreo.getText(), txtUsuario.getText() ,"Vendedor");
            if (correcto){
                llenarTabla();
                JOptionPane.showMessageDialog(this, "Usuario Registrado con éxito.");
            }else{
                JOptionPane.showMessageDialog(this, "El usuario no puedo ser registrado.", "Error", 0);
            }
        }
    }//GEN-LAST:event_btnCrearMouseClicked

    private void btnEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEditarMouseClicked
        if (cnx.editarUSuario(txtDocumento.getText(), txtNombre.getText() , txtApellido.getText(), txtContrasena.getText(), txtTelefono.getText(), txtCorreo.getText(), txtUsuario.getText() ,"Vendedor", usuario.get(indiceArray).getId_usuario())){
            boolean correcto = true;
            if (correcto){
                llenarTabla();
                JOptionPane.showMessageDialog(this, "Usuario editado con éxito.");
            }else{
                JOptionPane.showMessageDialog(this, "El usuario no puedo ser editado.", "Error", 0);
            }
        }
    }//GEN-LAST:event_btnEditarMouseClicked

    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        if (cnx.eliminarUsuario(usuario.get(indiceArray).getId_usuario())) {
            JOptionPane.showMessageDialog(this, "Usuario eliminado exitosamente.");
            llenarTabla();
        }else{
            JOptionPane.showMessageDialog(this, "El usuario no pudo ser eliminado.", "Error", 0);
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

    private void tblVendedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblVendedorMouseClicked
        indiceTabla = Integer.parseInt(tabla.getValueAt(tblVendedor.getSelectedRow(), 0).toString()) - 1; 
        for (int i = 0; i < usuario.size(); i++) {
            if (indiceTabla + 1 == usuario.get(i).getId_usuario()) {
                indiceArray = i;
            }
        }
        txtDocumento.setText(usuario.get(indiceArray).getDocumento());
        txtNombre.setText(usuario.get(indiceArray).getNombre());
        txtApellido.setText(usuario.get(indiceArray).getApellido());
        txtUsuario.setText(usuario.get(indiceArray).getUsuario());
        txtContrasena.setText(usuario.get(indiceArray).getContrasena());
        txtTelefono.setText(usuario.get(indiceArray).getTelefono());
        txtCorreo.setText(usuario.get(indiceArray).getEmail());
    }//GEN-LAST:event_tblVendedorMouseClicked


