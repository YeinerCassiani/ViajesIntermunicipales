    public void horaFechaViaje(){
        for (Rutas rutas : rutasfiltradas) {
            if (rutas.getOrigen().equals(cbOrigen.getSelectedItem().toString())) {
                if (rutas.getDestino().equals(cbDestino.getSelectedItem().toString())) {
                    cbFechaSalida.addItem(rutas.getFechaViaje().toString());
                    cbHoraSalida.addItem("" + rutas.getHoraViaje());
                    valor = rutas.getCosto();
                }
            }
        }
    }
    
    public void quitarAnimacion(){
        panelFondo.setVisible(true);
        limpiar();
        lbAnimacion.setVisible(false);
    }
    
    public void ejecutarAnimacion(){
        ScheduledExecutorService service = Executors.newSingleThreadScheduledExecutor();
        service.schedule(this::generarReporte, 1, TimeUnit.SECONDS);
        service.schedule(this::quitarAnimacion, 2, TimeUnit.SECONDS);
        cnx.crearTiquete(lbOrigen.getText(), lbDestino.getText(), lbValor.getText(), lbFechaSalida.getText(), lbHoraSalida.getText(), Integer.parseInt(lbIDCliente.getText()));
        panelFondo.setVisible(false);
        lbAnimacion.setVisible(true);
        
    }
    
    public void generarReporte(){
        try {
            JasperReport jasperReport = JasperCompileManager.compileReport(informeOrigen);
            JasperPrint jasperPrint = JasperFillManager.fillReport(jasperReport,null, cnx.conectar());
            JasperExportManager.exportReportToPdfFile(jasperPrint, informeDestino);
            JasperViewer.viewReport(jasperPrint, false);
        } catch (JRException ex) {
            System.err.println(ex.getMessage());
        }
    }
    
