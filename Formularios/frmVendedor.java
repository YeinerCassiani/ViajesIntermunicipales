    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        limpiar();
        if (txtDocumento.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "El campo documento está vacío.", "Error", 0);
        }else{
            deshabilitar();
            cliente.addAll(cnx.consultarUsuario("Cliente"));
            for (int i = 0; i < cliente.size(); i++) {
                if (cliente.get(i).getDocumento().equals(txtDocumento.getText())) {
                    lbPasajero.setText(cliente.get(i).getNombre() + " " + cliente.get(i).getApellido());
                    lbDocumento.setText(cliente.get(i).getDocumento());
                    txtNombre.setText(cliente.get(i).getNombre());
                    txtApellido.setText(cliente.get(i).getApellido());
                    txtEmail.setText(cliente.get(i).getEmail());
                    txtTelefono.setText(cliente.get(i).getTelefono());
                    txtContrasena.setText(cliente.get(i).getContrasena());
                    lbIDCliente.setText("" + cliente.get(i).getId_usuario());
                }
            }
            if (lbPasajero.getText().equals("")) {
                habilitar();
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        if (!txtDocumento.getText().equals("") && !txtNombre.getText().equals("") && !txtApellido.getText().equals("") && !txtEmail.getText().equals("") && !txtTelefono.getText().equals("") && !txtContrasena.getText().equals("")) {
            boolean correcto = cnx.ingresarUSuario(txtDocumento.getText(), txtNombre.getText() + " " + txtApellido.getText(), txtContrasena.getText(), txtTelefono.getText(), txtEmail.getText(),txtDocumento.getText() ,"Cliente");
            if (correcto){
                JOptionPane.showMessageDialog(null, "Usuario Registrado con éxito.");
                deshabilitar();
                limpiar();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Hay uno o más campos vacios.", "Error", 0);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

