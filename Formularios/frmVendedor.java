    private void cbOrigenItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbOrigenItemStateChanged
        if (cbOrigen.getSelectedIndex() > 0 && cbDestino.getSelectedIndex() > 0) {
            cbFechaSalida.removeAllItems();
            cbHoraSalida.removeAllItems();
            horaFechaViaje();
            if (cbOrigen.getSelectedIndex() != cbDestino.getSelectedIndex()) {
                lbOrigen.setText(cbOrigen.getSelectedItem().toString());
                lbDestino.setText(cbDestino.getSelectedItem().toString());
                lbValor.setText(valor);
            }else{
                cbOrigen.setSelectedIndex(0);
                JOptionPane.showMessageDialog(this, "El origen y destino no pueden ser iguales.", "Error", 0);
            }
        }
    }//GEN-LAST:event_cbOrigenItemStateChanged

